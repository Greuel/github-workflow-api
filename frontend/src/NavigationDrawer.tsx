const mdTheme = createTheme();

function NavigationDrawer() {
  const [open, setOpen] = React.useState(true);
  const [drawerPadding, setDrawerPadding] = useState(0);
  const measuredDrawerRef = React.useCallback((node: HTMLDivElement | null) => {

    if (!node) {

      setDrawerPadding(0);

      return;

    }

    if (node !== null && node.firstChild) {

      const drawerElement = node.firstChild as HTMLElement;

      setDrawerPadding(drawerElement.clientWidth);

    }

  }, []);
  const toggleDrawer = () => {
    setOpen(!open);
  };

  return (
    <ThemeProvider theme={mdTheme}>
      <Box sx={{ display: 'flex' }}>
        <CssBaseline />
        <AppBar position="absolute" open={open}>
          <Toolbar
            sx={{
              pr: '24px', // keep right padding when drawer closed
            }}
          >
            <IconButton
              edge="start"
              color="inherit"
              aria-label="open drawer"
              onClick={toggleDrawer}
              sx={{
                marginRight: '36px',
                ...(open && { display: 'none' }),
              }}
            >
              <MenuIcon />
            </IconButton>
            <Typography
              component="h1"
              variant="h6"
              color="inherit"
              noWrap
              sx={{ flexGrow: 1 }}
            >
              Workflow Dashboard
            </Typography>
          </Toolbar>
        </AppBar>
        <Drawer variant="permanent" open={open}>
          <Toolbar
            sx={{
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'flex-end',
              px: [1],
            }}
          >
            <IconButton onClick={toggleDrawer}>
              <ChevronLeftIcon />
            </IconButton>
          </Toolbar>
          <Divider />
          <List component="nav">
            {mainListItems}
            <Divider sx={{ my: 1 }} />
            {secondaryListItems}
          </List>
        </Drawer>
      </Box>
      <main style={{ marginTop: '7rem', paddingLeft: '${drawerPadding}px' }}>
        <Routes>
          <Route path="/" element= {<Dashboard />} />
          <Route path="/workflows" element={<div>Hi blablablablablalbalblabalbla</div>} />
        </Routes>
      </main>
    </ThemeProvider>
  );
}

